//! Concatination means joining/combining two or more operands. We use + operator for Concatination.
//! number + number --> addition   --> 1+1 = 2
//! number + String --> Concatination   --> 1+"Hi" = "1Hi"
//! String + number --> Concatination   --> "Hi"+1 = "Hi1"
//! String + String --> Concatination   --> "Hi"+"Hello" = "HiHello"
public class Concatination {
    public static void main(String[] args) {
        System.out.println(1+1);
        System.out.println(1+"Hi");
        System.out.println("Hi"+1);
        System.out.println("Hi"+"Hello");
    }    
}
