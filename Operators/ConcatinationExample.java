public class ConcatinationExample {
    public static void main(String[] args) {
        int a = 11;
        int b = 2;
        int res = a+b;
        System.out.println(res);
        System.out.println("Addition of two numbers is " +res);
        System.out.println(a + "+" + b +"=" + res);
    }    
}
